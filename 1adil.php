					<h2>Dům soumraku I</h2>
					<p> Chtěl bych vám vyprávět příběh, který se mohl stát. Odehrává se ve fiktivním Městě v blíže nespecifikované blízké budoucnosti. Zatímco svět začíná ohrožovat epidemie nebezpečné smrtící chřipky nazývané „tichá“, formuje se zvláštní společenství lidí. Říkají si „Stráže zdraví s štěstí“ a tvrdí, že dokážou vyléčit všechny nemoci. Když epidemie dorazí i do Města a do karantény je uzavřeno nové sídliště na ostrůvku uprostřed metropole, vydávají se do něj neprozřetelně dva dávní přátelé.</p>
     <p>Skutečnosti, na které narazí a o nichž předtím neměli ani tušení, je strhnou do víru událostí, kde jde nejen o jejich svobodu, ale i o život.</p>
